// ④ 
// 変数$strに文字列 "ボクの名前はリオです。" を代入
$str = "ボクの名前はリオです。"; 
// str_replace関数で置き換える部分の文字列を変数$replace_strに配列で代入
$replace_str = array('ボク', 'リオ');
// str_replace関数で置き換え後の文字列を変数$bk_replace_strに配列で代入
$bk_replace_str = array('私', '凛生');
// str_replace関数で変数$strの文字列から変数$replace_strの置き換える部分の文字列を変数$bk_replace_strの文字列に置き換えしecho関数で出力
echo str_replace($replace_str, $bk_replace_str, $str);


