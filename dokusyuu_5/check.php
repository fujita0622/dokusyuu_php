// ①
// 位置検索の対象となる文字列を$strに代入
$str = "PHPはPHP:Hypertext Preprocessorの略です";
// mb_strrpos関数で第一引数の文字列から第二引数で指定した'PHP'という文字列の最後に登場する位置を特定しecho関数で出力
echo mb_strrpos($str, 'PHP');

// ② 文字列「●◯の気温は●◯℃です。」という書式文字列に「弘前」「15.156」という数値を埋め込む。ただし、数値は小数点以下1桁までを表示する
// 文字列「●◯の気温は●◯℃です。」をprintf関数用に返還指定子を置き換えて$strに代入
$str = "%sの気温は%.1f℃です";
// printf関数で変数$strの処理機文字列に第二引数で設定した値を順に置き換えし出力
printf($str, "弘前", "15.156");

// ③ 文字列「wings knowledge」を、単語の頭文字だけ大文字に変換する
